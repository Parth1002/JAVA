package com.company;

public class Pa_09_switch {
    public static void main(String[] args) {
        int ch=2;

        switch (ch)
        {
            case 1:
                System.out.println("Frist");
                break;

            case 2:
                System.out.println("second");
                break;

            case 3:
                System.out.println("Thard");
                break;

            default:
                System.out.println("value not number");
                break;
        }
    }
}
